    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                if (jRadioButton1.isSelected()) {
                    gameMode = 0;
                }
                if (jRadioButton2.isSelected()) {
                    gameMode = 1;
                }
                if (jRadioButton3.isSelected()) {
                    if (jCheckBox1.isSelected()){
                        JOptionPane.showMessageDialog(null, "Educational mode is not yet available for Internet Multiplayer!");
                        return;
                    }
                    gameMode = 2;
                }
                snakes = Integer.parseInt(label3.getText());
                ladders = Integer.parseInt(label2.getText());
                stars = Integer.parseInt(label1.getText());
                if (jCheckBox1.isSelected()){
                    educationalMode = true;
                }
                finished = true;
                setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

